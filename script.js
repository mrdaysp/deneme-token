document.getElementById('connectWallet').addEventListener('click', async () => {
    if (window.ethereum) {
        web3 = new Web3(window.ethereum);
        try {
            await window.ethereum.request({ method: 'eth_requestAccounts' });
            const accounts = await web3.eth.getAccounts();
            userAddress = accounts[0];
            
            // Cüzdan bilgilerini göster
            document.getElementById('walletInfo').style.display = 'block';
            document.getElementById('address').textContent = userAddress;
            
            // Bakiyeleri güncelle
            updateBalances();
            
            // Satın alma bölümünü aç
            document.getElementById('buySection').style.display = 'block';
        } catch (error) {
            console.error("Cüzdan bağlanamadı:", error);
        }
    } else {
        alert("Lütfen MetaMask yükleyin!");
    }
});

async function updateBalances() {
    // tBNB Bakiyesi
    const bnbBalance = await web3.eth.getBalance(userAddress);
    document.getElementById('bnbBalance').textContent = web3.utils.fromWei(bnbBalance, 'ether') + " tBNB";
    
    // FEN Bakiyesi
  tokenContract = new web3.eth.Contract(tokenABI, TOKEN_ADDRESS);
const tokenBalance = await tokenContract.methods.balanceOf(userAddress).call();
const decimals = await tokenContract.methods.decimals().call(); // Decimal değerini al
const formattedBalance = (tokenBalance / (10 ** decimals)).toLocaleString();
document.getElementById('tokenBalance').textContent = formattedBalance + " FENO";
}

function calculateBNB() {
    const tokenAmountInput = document.getElementById('tokenAmount').value;
    
    try {
        // Token'ın 9 decimal olduğunu varsayarak çevirim yapıyoruz
        const tokenAmountBase = web3.utils.toBN(tokenAmountInput);
        const decimals = web3.utils.toBN(10).pow(web3.utils.toBN(9)); // 10^9
        const tokenAmountWei = tokenAmountBase.mul(decimals);

        // Fee hesapla: tokenAmount * 3%
        const feeWei = tokenAmountWei.mul(web3.utils.toBN(3)).div(web3.utils.toBN(100));

        // Toplam token miktarı (wei cinsinden)
        const totalTokensWei = tokenAmountWei.add(feeWei);

        // BNB hesaplama (totalTokensWei / 30000) - 300$ = 30000 * 0.01$
        const requiredBNBWei = totalTokensWei.mul(web3.utils.toBN(1e18)).div(web3.utils.toBN(30000));

        // Sonucu BNB cinsinden göstermek için wei'den çevirim
        const bnbAmount = web3.utils.fromWei(requiredBNBWei, 'ether');
        document.getElementById('requiredBNB').textContent = `${bnbAmount} BNB (${requiredBNBWei.toString()} wei)`;
    } catch (error) {
        console.error(error);
        document.getElementById('requiredBNB').textContent = "Hesaplama hatası!";
    }
}


//Yenilenmiş fonksiyon
async function buyTokens() {
    const tokenAmountInput = document.getElementById('tokenAmount').value;
    const tokenAmount = parseFloat(tokenAmountInput);
    
    if (isNaN(tokenAmount)) {
        alert("Lütfen geçerli bir token miktarı girin!");
        return;
    }

    saleContract = new web3.eth.Contract(saleABI, SALE_ADDRESS);
    
    // Fee ve BNB hesaplamaları (decimal 9'a göre)
    const fee = tokenAmount * 0.03;
    const totalTokens = tokenAmount + fee;
    const requiredBNB = (totalTokens * 0.01) / 300;
    
    // Token miktarını 9 decimal (gwei) formatına çevirme
    const tokenAmountInWei = web3.utils.toWei(tokenAmount.toString(), 'gwei');
    
    // BNB miktarını 18 decimal (ether) formatına çevirme
    const bnbAmountInWei = web3.utils.toWei(requiredBNB.toString(), 'ether');

    try {
        await saleContract.methods.buyTokens(tokenAmountInWei).send({
            from: userAddress,
            value: bnbAmountInWei,
            gas: 300000
        });
        alert("Satın alma başarılı!");
        updateBalances();
    } catch (error) {
        console.error("Hata:", error);
        alert("Satın alma işlemi başarısız oldu!");
    }
}
