async function connectWallet() {
    if (window.ethereum) {
        web3 = new Web3(window.ethereum);
        try {
            await window.ethereum.request({ method: 'eth_requestAccounts' });
            const accounts = await web3.eth.getAccounts();
            userAddress = accounts[0];
            updateWalletInfo();
        } catch (error) {
            console.error("Cüzdan bağlantı hatası:", error);
        }
    } else {
        alert("Lütfen MetaMask yükleyin!");
    }
}

async function updateWalletInfo() {
    const balance = await web3.eth.getBalance(userAddress);
    document.getElementById('walletInfo').innerHTML = `
        <p>Adres: ${userAddress}</p>
        <p>tBNB Bakiye: ${web3.utils.fromWei(balance, 'ether')}</p>
    `;
}

async function buyTokens() {
    const tokenAmount = document.getElementById('tokenAmount').value;
    const contract = new web3.eth.Contract(contractABI, contractAddress);
    const price = await contract.methods.pricePerToken().call();
    const requiredBnb = (tokenAmount * price * 103) / 100;

    contract.methods.buyTokens(tokenAmount).send({
        from: userAddress,
        value: requiredBnb
    })
    .on('transactionHash', (hash) => {
        document.getElementById('transactionStatus').innerHTML = `İşlem hash: ${hash}`;
    })
    .on('receipt', (receipt) => {
        document.getElementById('transactionStatus').innerHTML = "Başarılı!";
    })
    .on('error', (error) => {
        document.getElementById('transactionStatus').innerHTML = "Hata: " + error.message;
    });
}

document.getElementById('connectButton').addEventListener('click', connectWallet);
document.getElementById('buyButton').addEventListener('click', buyTokens);
